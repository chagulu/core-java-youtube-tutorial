public class LoopsExample {

    public static void main (String[] args){

        for (int i = 5; i <=10; i++){
            System.out.println("Fro loop : " + i);
        }
    

    int xyz =10;
    while(xyz <=5){
        System.out.println("while loop : " + xyz);
        xyz ++;
    }




    int abc =10;

    do {
        System.out.println("do while loop : " + abc);
        abc ++;
    }while (abc <=5 );

    
    
}


}
